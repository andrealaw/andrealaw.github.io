/*

Learn the if statement

In Java, 
- the keyword if is the first part of a conditional expression
- followed by a Boolean expression
- then a block of code

If the Boolean expression evaluates to true, the block of code that follows will be run.

The if statement
- is not followed by a semicolon (;)
- uses curly braces ({ and }) to surround the code block that gets run when the Boolean expression is true

*/

public class If {
	public static void main(String[] args) {

        // Task: Provide the if statement with a Boolean expression that evaluates to true
		if (4 > 2) {

			System.out.println("Access granted.");

		}
		
	}
}