public class DataTypes {
	public static void main(String[] args) {

        /**
        Datatype: int
        - is short for integer
        - are all positive and negative numbers, including zero. This number could represent the number of visits a website has received or the number of programming languages you know.
        - allows values between -2,147,483,648 and 2,147,483,647
        - does not need to be typed within quotes 
        */
		System.out.println(2017);

        /**
        Datatype: boolean
        - a data type that can only be either true or false
        */
        System.out.println(false);

         /**
        Datatype: char
        - is short for character
        - can represent a single character
        - must be enclosed in single quotes, like this: 'G'
        */
        System.out.println('M');
	}
}